const supportedLocales = Object.keys(copy);
const storageKey = "northstar-world-locale";

function normalizeLocale(candidate) {
  if (!candidate) {
    return null;
  }

  if (supportedLocales.includes(candidate)) {
    return candidate;
  }

  const language = candidate.split("-")[0];
  return supportedLocales.find((locale) => locale.split("-")[0] === language) || null;
}

function safeStorageGet(key) {
  try {
    return window.localStorage.getItem(key);
  } catch {
    return null;
  }
}

function safeStorageSet(key, value) {
  try {
    window.localStorage.setItem(key, value);
  } catch {
    // Ignore storage failures so the page still works in restricted environments.
  }
}

function getInitialLocale() {
  const fromQuery = normalizeLocale(new URLSearchParams(window.location.search).get("lang"));
  if (fromQuery) {
    return fromQuery;
  }

  const fromStorage = normalizeLocale(safeStorageGet(storageKey));
  if (fromStorage) {
    return fromStorage;
  }

  const browserLocales = Array.isArray(navigator.languages) ? navigator.languages : [navigator.language];
  const browserMatch = browserLocales.map(normalizeLocale).find(Boolean);
  return browserMatch || "zh-CN";
}

function getText(locale) {
  return copy[locale] || copy["en-US"];
}

function applyCopy(locale) {
  const text = getText(locale);
  document.title = text.documentTitle;
  document.documentElement.lang = locale;
  document.documentElement.dir = locale.startsWith("ar") ? "rtl" : "ltr";

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (text[key]) {
      element.textContent = text[key];
    }
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
    const key = element.dataset.i18nAria;
    if (text[key]) {
      element.setAttribute("aria-label", text[key]);
    }
  });

  document.querySelectorAll("[data-option-key]").forEach((option) => {
    const key = option.dataset.optionKey;
    if (text[key]) {
      option.textContent = text[key];
    }
  });

  metricRevenueHint.textContent = text.metricRevenueHint;
  metricAudienceHint.textContent = text.metricAudienceHint;
  metricLaunchHint.textContent = text.metricLaunchHint;
}

function formatCompact(locale, value) {
  return new Intl.NumberFormat(locale, {
    notation: "compact",
    maximumFractionDigits: 1,
  }).format(value);
}

function updateMetrics(locale, regionKey) {
  const text = getText(locale);
  const region = regionData[regionKey];

  metricRevenueValue.textContent = new Intl.NumberFormat(locale, {
    style: "currency",
    currency: region.currency,
    notation: "compact",
    maximumFractionDigits: 1,
  }).format(region.revenue);

  metricAudienceValue.textContent = `${formatCompact(locale, region.audience)} ${text.audienceSuffix}`;
  metricLaunchValue.textContent = `${new Intl.NumberFormat(locale).format(region.launchDays)} ${text.launchDays}`;
}

function updateClocks(locale, regionKey) {
  const text = getText(locale);
  const region = regionData[regionKey];

  region.cities.forEach((city, index) => {
    const slot = clockElements[index];
    if (!slot) {
      return;
    }

    slot.label.textContent = `${city.name} · ${text.clockLabel}`;
    slot.value.textContent = new Intl.DateTimeFormat(locale, {
      timeZone: city.timeZone,
      hour: "numeric",
      minute: "2-digit",
      weekday: "short",
    }).format(new Date());
  });
}

function render(locale, regionKey) {
  applyCopy(locale);
  updateMetrics(locale, regionKey);
  updateClocks(locale, regionKey);
  safeStorageSet(storageKey, locale);
}

const initialLocale = getInitialLocale();
localeSelect.value = initialLocale;
regionSelect.value = "global";
render(initialLocale, regionSelect.value);

localeSelect.addEventListener("change", (event) => {
  render(event.target.value, regionSelect.value);
});

regionSelect.addEventListener("change", (event) => {
  render(localeSelect.value, event.target.value);
});

window.setInterval(() => {
  updateClocks(localeSelect.value, regionSelect.value);
}, 30000);
